def sum(a, b):
    """This function takes as parameters two ints a and b and returns their sum.

    Parameters
    ----------
    a : int
        The first parameter.
    b : int
        The second parameter.

    Returns
    -------
    int
        It is the sum of a and b
    
    Examples
    --------
    >>> print(sum(3,4))
    7

    """
    return a + b

print(sum(3,4))
